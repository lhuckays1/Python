def adicao(a,b):
    soma = int(a+b)
    print(f"A soma do número {a} + {b} é {soma}")

def subtracao(a,b):
    subtracao = int(a-b)
    print(f"A subtração do número {a} - {b} é {subtracao}")

def multiplicacao(a,b):
    multiplicacao = int(a,b)
    print(f"A multiplicação do número {a} x {b} é {multiplicacao}")

def divisao(a,b):
    divisao = int(a/b)
    print(f"A divisão do número {a} / {b} é {divisao}")