n1 = float(input("Digite a 1ª nota do aluno: "))
n2 = float(input("Digite a 2ª nota do aluno: "))
n3 = float(input("Digite a 3ª nota do aluno: "))

media = float((n1 + n2 + n3)/3)

print("A média desse aluno é " + str(media) + " pontos")

n4 = float(input("Digite a 4ª nota do aluno: "))

media = float((n1 + n2 + n3 + n4)/4)

print("A nova média será " + str(media) + " pontos")